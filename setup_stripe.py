def check_requirements():
    """Check if required packages are installed"""
    required_packages = ['stripe', 'flask', 'python-dotenv']
    missing_packages = []
    
    for package in required_packages:
        try:
            __import__(package.replace('-', '_'))
        except ImportError:
            missing_packages.append(package)
    
    if missing_packages:
        print("âŒ Missing required packages:")
        for package in missing_packages:
            print(f"   - {package}")
        print(f"\nInstall with: pip install {' '.join(missing_packages)}")
        return False
    
    return True

def test_stripe_connection():
    """Test Stripe API connection"""
    try:
        import stripe
        from dotenv import load_dotenv
        
        # Load environment variables
        load_dotenv()
        
        # Get Stripe key
        stripe_key = os.getenv('STRIPE_SECRET_KEY')
        if not stripe_key:
            print("âŒ STRIPE_SECRET_KEY not found in environment")
            return False
        
        # Set Stripe key
        stripe.api_key = stripe_key
        
        # Test API call
        print("ğŸ§ª Testing Stripe connection...")
        account = stripe.Account.retrieve()
        
        print(f"âœ… Connected to Stripe successfully!")
        print(f"ğŸ“Š Account ID: {account.id}")
        print(f"ğŸ¢ Business Name: {account.business_profile.name if account.business_profile else 'Not set'}")
        print(f"ğŸŒ Country: {account.country}")
        print(f"ğŸ’³ Charges Enabled: {account.charges_enabled}")
        print(f"ğŸ’° Payouts Enabled: {account.payouts_enabled}")
        
        return True
        
    except ImportError:
        print("âŒ Stripe package not installed. Run: pip install stripe")
        return False
    except Exception as e:
        print(f"âŒ Stripe connection failed: {e}")
        return False

